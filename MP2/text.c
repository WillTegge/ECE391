/*
 * text_to_graphics
 *   DESCRIPTION: Produces a buffer that holds graphical image of the ASCII characters in the string. Fixed height buffer. 
 *   INPUTS: string_input -- string that holds the ascii characters to put on the buffer
             buf -- buffer of fixed size to store the graphical images of the string
             forground_color - color of the text 
             background_color - color of background 
 *   OUTPUTS: none
 *   RETURN VALUE: Returns 0 on success.  If x is outside of the valid
 *                 SCROLL range, the function returns -1.
 *   SIDE EFFECTS: draws into the build buffer
 */
void text_to_graphics(const char* string_input, unsigned char* buf, int foreground_color, int background_color){

    // algorithm
    // loop through the length of the string
        // loop through each row
            // loop through each pixel in the current row   

    // look at each bit
    // if 0 -> background, if 1 -> text

    int i;
    int j;
    int k;          // get # of characters in the input string
    int bit_value;
    int current_byte;

    int bit_shift = 0x80;               // "bit mask" -> 1000 0000 

    int length_of_str = strlen(string_input);

    int ctr = (STATUS_BAR_X_DIM - (length_of_str * FONT_WIDTH)) / 2;    // (320 - (str * 8)) / 2
    // 160 - (str * 4)

    unsigned char temp_buf[320 * 18];     // 320 is the status bar width and 18 is the # of characters that can fit into it


    // memset(buf, (0x0A + 2), 5760);          // 5760 since that is how many pixels are in the status bar
    // memset(temp_buf, (0x0A + 2), 5760);     // cp1

    memset(temp_buf, background_color, 5760);       // cp2


    // alternate method suggested by TA would be to fill in the buffer with background color first

    for(k = 0; k < length_of_str; k++){     // loop through each character
        // get the first bit of current row into font data
        bit_value = (int)string_input[k];   

        for(j = 0; j < 16; j++){            // 16 = FONT_HEIGHT -> looping through rows
            // get each byte (row)
            current_byte = font_data[bit_value][j];
            bit_shift = 0x80;

            for(i = 0; i < 8; i++){         // 8 = FONT_WIDTH -> looping through columns
                if(i == 0){
                    bit_shift = 0x80;
                }
                if(current_byte & bit_shift){      // if its a 1, then write the foreground color into the buf
                    // temp_buf[ctr + (i + ((j+1)*STATUS_BAR_X_DIM)) + (k*FONT_WIDTH)] = (0x00);       // foreground color   (cp1)
                    temp_buf[ctr + (i + ((j+1)*STATUS_BAR_X_DIM)) + (k*FONT_WIDTH)] = foreground_color;    // (cp2)

                }
                
                bit_shift = bit_shift >> 0x01;      // same as 0x1. Shifting the mask to the next bit 

            }
        }
    }

    int a;
    // split into planes from temp_buf to buf
    for(a = 0; a < 1440; a++){                      // 1440 is the size
        buf[a] = temp_buf[4 * a];

        buf[(a+1440)] = temp_buf[(4 * a) + 1];      // 1440 is the size (how many pixels)

        buf[(a+2880)] = temp_buf[(4 * a) + 2];      // 1440 * 2 is the size
        
        buf[(a+4320)] = temp_buf[(4 * a) + 3];      // 1440 * 3 is the size

    }

}


/*
 * transparent_text_to_graphics
 *   DESCRIPTION: Produces a buffer that holds graphical image of the ASCII characters in the string accpet this one shows
 *                transparent colors. Fixed height buffer. 
 *   INPUTS: string_input -- string that holds the ascii characters to put on the buffer
 *           buf -- buffer of fixed size to store the graphical images of the string
             trans_buf -- buffer of fixed size that stores the transparent version of the buffer passed in
 *   OUTPUTS: none
 *   RETURN VALUE: none
 *   SIDE EFFECTS: draws into the build buffer 
 */
void transparent_text_to_graphics(const char* string_input, unsigned char* buf, unsigned char* trans_buf){

    int string_spot;
    int i;
    int j;
    int MAX_CHARS = 96/8;           // 96/8 is length of screen over char length
    int ctr = ((MAX_CHARS - strlen(string_input))/2)*8;                    

    unsigned char font_byte;
    int string_;
    int shift_bit = 0x01;

    memcpy(buf, trans_buf, 1536);       // 1536 is FONT_HEIGHT * length of the screen

    for(string_spot = 0; string_spot < strlen(string_input); string_spot++){

        string_ = (int)(string_input[string_spot]);

        for(i = 0; i < FONT_HEIGHT; i++){
            
            font_byte = font_data[string_][i];

            for(j = 0; j < FONT_WIDTH; j++){

                if(j == 0){
                    shift_bit = 0x80;           // reset the bit shift value to start at the left most bit -> 1000 0000 

                }

                if((font_byte) & shift_bit){

                    // store into temp buffer and center it
                    // 96 is the length of the screen
                    // 64 is the offset to the next spot (color)
                    buf[ctr + string_spot * (FONT_WIDTH) + ((i*96)+j)] = trans_buf[ctr + string_spot * (FONT_WIDTH) + ((i*96)+j)] + 64;     

                }

                shift_bit = shift_bit >> 0x1;           // shift the bit over by one to the right
                
            }
        
        }
    }

    
}





